const RTS_FIXED_TEMPLATE_COUNT = RTS_FIXED_TEMPLATE_ENTRIES.length;

export function getRTSFixedTemplateCount() {
  return RTS_FIXED_TEMPLATE_COUNT;
}

export function getRTSFixedTemplateByQuestionOrder(order = 0) {
  const normalized = Number(order);
  if (!Number.isFinite(normalized)) return null;

  const index = Math.floor(normalized) - 1;
  if (index < 0) return null;

  const matched = RTS_FIXED_TEMPLATE_ENTRIES[index] || null;
  if (!matched) return null;

  return {
    order: Number(matched.order || index + 1),
    opener: `${matched.opener || ""}`.trim(),
    full: `${matched.full || ""}`.trim()
  };
}

export function getRTSFixedTemplateByQuestionIndex(index = -1) {
  const normalized = Number(index);
  if (!Number.isFinite(normalized)) return null;

  return getRTSFixedTemplateByQuestionOrder(Math.floor(normalized) + 1);
}
